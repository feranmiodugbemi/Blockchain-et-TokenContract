token_contract = web3.eth.contract(address=contract_address, abi=contract_abi)


##Function to get details of token
def get_token_details():
    name = token_contract.functions.name().call()
    symbol = token_contract.functions.symbol().call()
    decimals = token_contract.functions.decimals().call()
    total_supply = token_contract.functions.totalSupply().call()

    print(f'Token Name: {name}')
    print(f'Token Symbol: {symbol}')
    print(f'Decimals: {decimals}')
    print(f'Total Supply: {total_supply}')

#Function to get balance
def get_balance(address):
    balance = token_contract.functions.balanceOf(address).call()
    print(f'Balance: {balance}')


def transfer_tokens(sender, recipient, amount, contract, private_key):
    # Get the nonce. Prevents one from sending the transaction twice
    nonce = web3.eth.get_transaction_count(sender)

    # Build the transaction
    tx = contract.functions.transfer(recipient, amount).call()

    # Sign the transaction
    signed_tx = web3.eth.account.sign_transaction(tx, private_key)

    # Send the transaction
    tx_hash = web3.eth.send_raw_transaction(signed_tx.rawTransaction)

    # Wait for the transaction to be mined
    tx_receipt = web3.eth.waitForTransactionReceipt(tx_hash)

    return tx_receipt

